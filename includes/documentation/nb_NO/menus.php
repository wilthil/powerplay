<h1><?php _e('Menus','veuse');?></h1>
<p><?php echo $theme_name;?> takes advantage of the Wordpress 3.0 menu manager, which is located under <a href="nav-menus.php" target="blank">Appearance &raquo; Menus</a>.</p>

<p>The theme has two menu locations: Header menu and Footer menu.</p>

<p><b>The header menu</b> has a fallback function that builds the menu automatically for you if you have not assigned a menu to the menu-location in <a href="nav-menus.php" target="blank">Appearance &raquo; Menus</a>.</p>

<p>If you assign a menu to the location, this assigned menu will be displayed, overriding the automaticly built menu.</p>


<p><b>The footer menu</b> needs to be built manually and assigned to the footer menu location in  <a href="nav-menus.php" target="blank">Appearance &raquo; Menus</a>.</p>

